def gen_tool_result():
    from gemi.ui.theme import get_palette, get_rich_theme
    c = fresh_console(width=100, theme=get_rich_theme("dark"))
    p = get_palette("dark")

    c.print()
    c.print(f"  [bold {p.buddy_shimmer}]>[/] explain how the QueryEngine handles streaming")
    c.print()
    c.print(f"  ✻", style=f"bold {p.buddy_shimmer}")
    c.print()
    c.print(f"    Let me read the file to give you an accurate answer.")
    c.print()
    c.print(f"  [dim]⎯[/][bold {p.buddy}]◇[/][dim]⎯ [/]"
            f"[bold yellow]read_file[/]  [dim]gemi/query_engine.py[/]")
    c.print(f"  [dim]⎯[/][bold {p.success}]✓[/][dim]⎯ [/]"
            f"[bold yellow]read_file[/]  [dim]4.2KB, 156 lines · 0.04s[/]")

    code = """    def _stream_round(self, tools):
        payload = _build_payload(self.messages, ...)
        with self._client.stream("POST", "/v1/messages", json=payload) as resp:
            for line in resp.iter_lines():
                if line.startswith("data: "):
                    data = json.loads(line[6:])
                    if data.get("type") == "content_block_delta":
                        delta = data.get("delta", {})
                        if delta.get("type") == "text_delta":
                            chunk = delta.get("text", "")
                            self.on_text_chunk(chunk)
        return _collect_sse_to_response(events)"""
    syntax = Syntax(code, "python", theme="monokai", line_numbers=False, indent_guides=True)
    c.print(Panel(syntax, border_style=p.border, expand=False, padding=(0, 1)))

    c.print()
    c.print(f"    The streaming flow is straightforward:")
    c.print()
    c.print(f"    1. Open an `httpx.stream` POST to the proxy")
    c.print(f"    2. Iterate SSE events as they arrive")
    c.print(f"    3. For each `text_delta`, call `on_text_chunk` so the REPL")
    c.print(f"       renders incrementally")
    c.print(f"    4. After the stream ends, assemble all events into the")
    c.print(f"       complete response and check for tool_use blocks")
    c.print()
    c.print(f"  [muted]· 234 in / 91 out  ·  3.2s  ·  1 tool  ·  $0.0011[/]")
    c.print()

    save(c, "tool-result.svg", title="gemi -- streaming + tool call")


