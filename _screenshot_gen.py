"""Generate SVG screenshots of Gemi's UI for the README.

Uses rich.Console(record=True).export_svg() to capture real terminal output
as standalone, GitHub-renderable SVGs. Produces:
  assets/banner.svg            startup banner with agent info
  assets/picker.svg            agent picker UI
  assets/status.svg            fleet status table
  assets/help.svg              /help output
  assets/tool-result.svg       a sample tool call + result
  assets/streaming.svg         streaming response example
  assets/yolo-warning.svg      YOLO activation warning
"""
from __future__ import annotations
import sys
from pathlib import Path

ASSETS = Path(__file__).parent / "assets"
ASSETS.mkdir(exist_ok=True)
sys.path.insert(0, str(Path(__file__).parent))

from rich.console import Console
from rich.text import Text
from rich.panel import Panel
from rich.syntax import Syntax
from rich.table import Table
from rich.box import ROUNDED


def make_console(width: int = 100, height: int = 30) -> Console:
    """Recording console — output goes to memory, not stdout."""
    return Console(record=True, width=width, color_system="truecolor",
                   force_terminal=True, file=open(__file__, "rb"))  # placeholder


def fresh_console(width: int = 100, theme=None) -> Console:
    """A recording console that doesn't write to stdout."""
    import io
    return Console(record=True, width=width, color_system="truecolor",
                   force_terminal=True, file=io.StringIO(), theme=theme)


def save(c: Console, name: str, title: str = "Gemi") -> None:
    out = ASSETS / name
    svg = c.export_svg(title=title, code_format="""<svg class="rich-terminal" viewBox="0 0 {width} {height}" xmlns="http://www.w3.org/2000/svg">
    <!-- Generated by Rich for Gemi (https://github.com/space-kitty-o/gemi) -->
    <style>
        @import url("https://fonts.googleapis.com/css2?family=JetBrains+Mono:wght@400;700&display=swap");
        .{unique_id}-matrix {{
            font-family: JetBrains Mono, Cascadia Code, Consolas, monospace;
            font-size: {char_height}px;
            line-height: {line_height}px;
            font-variant-east-asian: full-width;
        }}
        .{unique_id}-title {{
            font-size: 18px;
            font-weight: bold;
            font-family: JetBrains Mono, sans-serif;
        }}
        {styles}
    </style>
    <defs>
        <clipPath id="{unique_id}-clip-terminal">
            <rect x="0" y="0" width="{terminal_width}" height="{terminal_height}" />
        </clipPath>
        {lines}
    </defs>
    {chrome}
    <g transform="translate({terminal_x}, {terminal_y})" clip-path="url(#{unique_id}-clip-terminal)">
    {backgrounds}
    <g class="{unique_id}-matrix">
    {matrix}
    </g>
    </g>
</svg>""")
    out.write_text(svg, encoding="utf-8")
    print(f"  wrote {out.name}  ({len(svg):,} bytes)")


